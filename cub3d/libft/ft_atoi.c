int	ft_atoi(const char *str)
{
	char			*a;
	long long int	res;
	int				b;

	a = (char *)str;
	res = 0;
	b = 1;
	while ((*a > 8 && *a < 14) || *a == ' ')
		a++;
	if (*a == '-' || *a == '+')
	{
		b = 44 - *a;
		a++;
	}
	while (*a != '\0' && (*a >= 48 && *a <= 57))
	{
		res = 10 * res + (*a - 48) * b;
		a++;
	}
	if ((int)res == res)
		return (res);
	return (b < 0 ? 0 : -1);
}
