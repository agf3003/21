#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *a;

	a = (char *)s;
	if (c == 0)
		return (a + ft_strlen(s));
	while (*a != '\0')
	{
		if (*a == c)
			return (a);
		a++;
	}
	return (NULL);
}
